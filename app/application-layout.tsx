async function fetchTrendingMarkets() {
  const response = await fetch('https://api.xcp.io/api/v1/trading-pair/trending', {
    method: 'GET',
  });

  if (!response.ok) {
    throw new Error('Failed to fetch trending markets');
  }

  const data = await response.json();
  return data.result;
}

